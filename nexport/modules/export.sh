    echo "  <div class='stat'><div class='stat-n'>${#PORT_DATA[@]}</div><div class='stat-l'>Total Ports</div></div>"
    echo "  <div class='stat'><div class='stat-n' style='color:#ff4444'>${risk_count[HIGH]:-0}</div><div class='stat-l'>High Risk</div></div>"
    echo "  <div class='stat'><div class='stat-n' style='color:#ffaa00'>${risk_count[MEDIUM]:-0}</div><div class='stat-l'>Medium Risk</div></div>"
    echo "  <div class='stat'><div class='stat-n' style='color:#ff6644'>${cve_ports}</div><div class='stat-l'>Ports with CVEs</div></div>"
    echo "  <div class='stat'><div class='stat-n'>${#cat_count[@]}</div><div class='stat-l'>Categories</div></div>"
    echo "</div>"

    echo "<table id='portTable'>"
    echo "<thead><tr><th>Port</th><th>Name</th><th>Protocol</th><th>Encrypted</th><th>Category</th><th>Risk</th><th>CVEs</th><th>Description</th></tr></thead>"
    echo "<tbody>"

    for key in $(echo "${!PORT_DATA[@]}" | tr ' ' '\n' | sort -n); do
      IFS='|' read -r port name proto enc cat desc detail risk cves exploits <<< "${PORT_DATA[$key]}"
      local cve_display="${cves}"
      [[ "$cves" == "-" || -z "$cves" ]] && cve_display="—"
      echo "<tr>"
      echo "  <td class='port'>$port</td>"
      echo "  <td class='name'>$name</td>"
      echo "  <td>$proto</td>"
      echo "  <td class='enc-${enc}'>$enc</td>"
      echo "  <td class='cat'>$cat</td>"
      echo "  <td class='risk-${risk}'>$risk</td>"
      echo "  <td class='cve'>$cve_display</td>"
      echo "  <td class='desc'>$desc</td>"
      echo "</tr>"
    done

    cat << 'HTMLFOOT'
</tbody>
</table>
<script>
function filterTable(){
  const v=document.getElementById('search').value.toLowerCase();
  const rows=document.querySelectorAll('#portTable tbody tr');
  rows.forEach(r=>{r.style.display=r.textContent.toLowerCase().includes(v)?'':'none'});
}
</script>
</body>
</html>
HTMLFOOT
  } > "$outfile"
}
